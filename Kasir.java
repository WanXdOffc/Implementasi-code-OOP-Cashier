public class Kasir {

    private String nama;
    private String idKasir;

    public Kasir(String nama, String idKasir) {
        this.nama    = nama;
        this.idKasir = idKasir;
    }

    public String getNama(){
        return nama;}
    public String getIdKasir(){
        return idKasir;
    }
}
